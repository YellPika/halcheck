#ifndef HALCHECK_GEN_CONTAINER_HPP
#define HALCHECK_GEN_CONTAINER_HPP

#include <halcheck/gen/group.hpp>
#include <halcheck/gen/next.hpp>
#include <halcheck/gen/shrink.hpp>
#include <halcheck/lib/optional.hpp>
#include <halcheck/lib/ranges.hpp>
#include <halcheck/lib/type_traits.hpp>

#include <cstddef>
#include <iterator>
#include <type_traits>

namespace halcheck { namespace gen {

/// @brief Generates a random container of elements.
/// @tparam T The type of container to generate.
/// @tparam F The type generator function.
/// @param gen A function generating optional elements.
/// @return An arbitrary container of elements generated by gen.
template<
    typename T,
    typename F,
    HALCHECK_REQUIRE(std::is_default_constructible<T>()),
    HALCHECK_REQUIRE(lib::is_insertable<T>()),
    HALCHECK_REQUIRE(lib::is_invocable<F>())>
T unfold(F gen) {
  auto _ = gen::group();

  T output;
  auto it = lib::end(output);
  while (auto x = gen::group(gen))
    it = std::next(output.insert(it, std::move(*x)));
  return output;
}

/// @brief Generates a random container value.
/// @tparam T The type of container to generate.
/// @tparam F A nullary function type.
/// @param gen A function producing random values for the container.
/// @return A random container whose elements are drawn from gen.
template<
    typename T,
    typename F,
    HALCHECK_REQUIRE(lib::is_insertable<T>()),
    HALCHECK_REQUIRE(lib::is_invocable_r<lib::range_value_t<T>, F>())>
T container(F gen) {
  auto size = gen::size();
  return gen::unfold<T>([&]() -> lib::optional<lib::range_value_t<T>> {
    while (true) {
      if (!gen::next(1, size))
        return lib::nullopt;

      --size;

      if (!gen::shrink())
        return gen::group(gen);
    }
  });
}

/// @brief Generates a random container value.
/// @tparam T The type of container to generate.
/// @tparam F A nullary function type.
/// @param size The number of elements to generate.
/// @param gen A function producing random values for the container.
/// @return A random container whose elements are drawn from gen.
template<
    typename T,
    typename F,
    HALCHECK_REQUIRE(lib::is_insertable<T>()),
    HALCHECK_REQUIRE(lib::is_invocable_r<lib::range_value_t<T>, F>())>
T container(std::size_t size, F gen) {
  return gen::unfold<T>([&]() -> lib::optional<lib::range_value_t<T>> {
    if (size == 0)
      return lib::nullopt;

    --size;
    return lib::invoke(gen);
  });
}

}} // namespace halcheck::gen

#endif
